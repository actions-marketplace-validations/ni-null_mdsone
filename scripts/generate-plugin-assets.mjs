#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const pluginsDir = path.join(projectRoot, "plugins");
const outFile = path.join(projectRoot, "src", "plugins", "generated", "plugin-assets.ts");

function toPosix(p) {
  return p.split(path.sep).join("/");
}

function stripBom(text) {
  return text.charCodeAt(0) === 0xfeff ? text.slice(1) : text;
}

function listFilesRecursive(absDir) {
  const out = [];
  const entries = fs.readdirSync(absDir, { withFileTypes: true });
  for (const entry of entries) {
    const absPath = path.join(absDir, entry.name);
    if (entry.isDirectory()) {
      out.push(...listFilesRecursive(absPath));
      continue;
    }
    if (!entry.isFile()) continue;
    out.push(absPath);
  }
  return out;
}

function collectPluginAssets() {
  const plugins = {};
  if (!fs.existsSync(pluginsDir)) return plugins;

  const entries = fs.readdirSync(pluginsDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  for (const pluginName of entries) {
    const assetsRoot = path.join(pluginsDir, pluginName, "assets");
    if (!fs.existsSync(assetsRoot) || !fs.statSync(assetsRoot).isDirectory()) continue;

    const css = {};
    const js = {};

    const files = listFilesRecursive(assetsRoot)
      .filter((absPath) => /\.(css|js)$/i.test(absPath))
      .sort((a, b) => a.localeCompare(b));

    for (const absPath of files) {
      const relPath = toPosix(path.relative(assetsRoot, absPath));
      const ext = path.extname(absPath).toLowerCase();
      const content = stripBom(fs.readFileSync(absPath, "utf8"));
      if (ext === ".css") {
        css[relPath] = content;
      } else if (ext === ".js") {
        js[relPath] = content;
      }
    }

    if (Object.keys(css).length === 0 && Object.keys(js).length === 0) continue;
    plugins[pluginName] = { css, js };
  }

  return plugins;
}

function renderModule(plugins) {
  const lines = [];
  lines.push("// AUTO-GENERATED FILE. DO NOT EDIT.");
  lines.push("// Generated by scripts/generate-plugin-assets.mjs");
  lines.push("");
  lines.push("export type GeneratedPluginAssetBuckets = {");
  lines.push("  css: Record<string, string>;");
  lines.push("  js: Record<string, string>;");
  lines.push("};");
  lines.push("");
  lines.push("export const GENERATED_PLUGIN_ASSETS: Record<string, GeneratedPluginAssetBuckets> = {");

  const pluginNames = Object.keys(plugins).sort((a, b) => a.localeCompare(b));
  for (const pluginName of pluginNames) {
    const bucket = plugins[pluginName];
    lines.push(`  ${JSON.stringify(pluginName)}: {`);
    lines.push("    css: {");
    for (const assetPath of Object.keys(bucket.css).sort((a, b) => a.localeCompare(b))) {
      lines.push(`      ${JSON.stringify(assetPath)}: ${JSON.stringify(bucket.css[assetPath])},`);
    }
    lines.push("    },");
    lines.push("    js: {");
    for (const assetPath of Object.keys(bucket.js).sort((a, b) => a.localeCompare(b))) {
      lines.push(`      ${JSON.stringify(assetPath)}: ${JSON.stringify(bucket.js[assetPath])},`);
    }
    lines.push("    },");
    lines.push("  },");
  }

  lines.push("};");
  lines.push("");
  return lines.join("\n");
}

function run() {
  const plugins = collectPluginAssets();
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, renderModule(plugins), "utf8");
  console.log(`[assets-gen] plugins: ${Object.keys(plugins).length}`);
  console.log(`[assets-gen] output: ${outFile}`);
}

run();
